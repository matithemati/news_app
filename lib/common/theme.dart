import 'package:flutter/material.dart';

class AppTheme {
  static final kPrimaryColor = Color(0xFF0A58ED);
  static final kAccentColor = Color(0xFF0A58ED);
  static final kBackgroundColor = Color(0xFFFFFFFF);

  static ThemeData kTheme = ThemeData(
